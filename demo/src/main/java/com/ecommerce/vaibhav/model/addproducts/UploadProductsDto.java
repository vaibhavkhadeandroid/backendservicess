package com.ecommerce.vaibhav.model.addproducts;

public class UploadProductsDto {
	
	
	private String productid;
	private String productname;
	private String productcatagory;
	private String sellingprice;
	private String costprice;
	private String discount;
	private String size;
	private String numberofitem;
	private String productimageurl;
	private String uploadproductdate;
	public String getProductid() {
		return productid;
	}
	public void setProductid(String productid) {
		this.productid = productid;
	}
	public String getProductname() {
		return productname;
	}
	public void setProductname(String productname) {
		this.productname = productname;
	}
	public String getProductcatagory() {
		return productcatagory;
	}
	public void setProductcatagory(String productcatagory) {
		this.productcatagory = productcatagory;
	}
	public String getSellingprice() {
		return sellingprice;
	}
	public void setSellingprice(String sellingprice) {
		this.sellingprice = sellingprice;
	}
	public String getCostprice() {
		return costprice;
	}
	public void setCostprice(String costprice) {
		this.costprice = costprice;
	}
	public String getDiscount() {
		return discount;
	}
	public void setDiscount(String discount) {
		this.discount = discount;
	}
	public String getSize() {
		return size;
	}
	public void setSize(String size) {
		this.size = size;
	}
	public String getNumberofitem() {
		return numberofitem;
	}
	public void setNumberofitem(String numberofitem) {
		this.numberofitem = numberofitem;
	}
	public String getProductimageurl() {
		return productimageurl;
	}
	public void setProductimageurl(String productimageurl) {
		this.productimageurl = productimageurl;
	}
	public String getUploadproductdate() {
		return uploadproductdate;
	}
	public void setUploadproductdate(String uploadproductdate) {
		this.uploadproductdate = uploadproductdate;
	}
}
